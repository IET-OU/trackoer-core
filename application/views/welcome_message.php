<h1><a name="trackoer-core" class="anchor" href="#trackoer-core"><span class="mini-icon mini-icon-link"></span></a>Track OER</h1>

<p><strong>Track OER</strong> is a <abbr title="Joint Information Systems Committee">JISC</abbr> and
<abbr title="Higher Education Funding Council for England">HEFCE</abbr>-funded project to facilitate Web analytics for Open Educational Resources.</p>

<p><a href="https://github.com/IET-OU/trackoer-core">trackoer-core</a> is the central web application for the Project, providing clean URL
redirection to no-Javascript web-bugs for <a href="http://piwik.org/">Piwik</a> (and Google Analytics), and test/demonstration pages.</p>

